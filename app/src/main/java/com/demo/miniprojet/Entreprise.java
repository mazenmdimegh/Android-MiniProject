package com.demo.miniprojet;

public class Entreprise {
    String nom;
    String secteur;
    String description;
    String taille;
    String lieu;
    String fondée;

    public String getNom() {
        return nom;
    }

    public String getSecteur() {
        return secteur;
    }

    public String getDescription() {
        return description;
    }

    public String getTaille() {
        return taille;
    }

    public String getLieu() {
        return lieu;
    }

    public String getFondée() {
        return fondée;
    }
}
